    private void abaddbookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_abaddbookActionPerformed
        // TODO add your handling code here:
        if(abtitle.getText().equals("") || abisbn.getText().equals("") || abauthor.getText().equals("") || abprice.getText().equals("") || abquantity.getText().equals("") || ablanguage.getText().equals("") || absummary.getText().equals("")){
            JOptionPane.showMessageDialog( null, "Make sure to fill up the entire form!" , "WARNING!", JOptionPane.WARNING_MESSAGE );
        }
        else{
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("BookStorePU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();
            Books b = em.find(Books.class, abisbn.getText());
            if(b != null){
                JOptionPane.showMessageDialog( null, "The ISBN refers to an already registered book!\nYou're either trying to add the same book twice or the ISBN given is invalid." , "Error!", JOptionPane.ERROR_MESSAGE );
                em.close();
                return;
            }
            Books book = new Books();
            book.setTitle(abtitle.getText());
            book.setAuthor(abauthor.getText());
            book.setBDescription(absummary.getText());
            book.setIsbn(abisbn.getText());
            book.setBLanguage(ablanguage.getText());
            book.setPrice(BigInteger.valueOf(Integer.valueOf(abprice.getText())));
            book.setQuantity(BigInteger.valueOf(Integer.valueOf(abquantity.getText())));
            book.setBookImage((Serializable) abimage.getIcon());

            em.persist(book);
            em.getTransaction().commit();
            em.close();}
    }//GEN-LAST:event_abaddbookActionPerformed

