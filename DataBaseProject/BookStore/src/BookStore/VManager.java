    private void fbsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fbsearchActionPerformed
        // TODO add your handling code here:
        if((fbisbn.getText().equals("")||fbisbn.getText().equals("ISBN")) &&(fbtitle.getText().equals("")||fbtitle.getText().equals("Title"))){
            JOptionPane.showMessageDialog( null, "At least one field must be filled!" , "WARNING!", JOptionPane.WARNING_MESSAGE );
            return;
        }

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("BookStorePU");
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();
        Books book = new Books();

        if((!fbisbn.getText().equals(""))&&(!fbisbn.getText().equals("ISBN"))){
            book = em.find(Books.class, fbisbn.getText());
            if(book == null){
                JOptionPane.showMessageDialog( null, "The entered ISBN doesn't refer to any book!" , "Error!", JOptionPane.ERROR_MESSAGE );
                em.close();
                return;
            }
        }
        else{

            boolean found = false;
            Query qry = em.createNamedQuery("Books.findByTitle");
            qry.setParameter("title", fbtitle.getText());
            List<Books> books = qry.getResultList();
            if(books.size() != 0){
                found = true;
            }
            boolean mresults = false;
            if(books.size()>1){
                mresults = true;
            }
            
            if(!found){
                JOptionPane.showMessageDialog( null, "The entered title doesn't refer to any book!" , "Error!", JOptionPane.ERROR_MESSAGE );
                em.close();
                return;

            }
            if(mresults){
                JOptionPane.showMessageDialog( null, "There are more than one book with this title!\nProvide the ISBN as well." , "Warning!", JOptionPane.WARNING_MESSAGE );
                em.close();
                return;

            }
            
            book = books.get(0);
        }

        efbooksearch.setVisible(true);
        ftitle.setText(book.getTitle());
        fisbn.setText(book.getIsbn());
        fauthor.setText(book.getAuthor());
        flanguage.setText(book.getBLanguage());
        fprice.setText(String.valueOf(book.getPrice()));
        fquantity.setText(String.valueOf(book.getQuantity()));
        fsummary.setText(book.getBDescription());
        if(book.getBookImage()!=null){
            fbimage.setIcon((Icon) book.getBookImage());
        }
        em.getTransaction().commit();
        em.close();

    }//GEN-LAST:event_fbsearchActionPerformed

